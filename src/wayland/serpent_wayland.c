Value wlButtonNative(int argCount, Value* args) {
    if (argCount < 6) return NIL_VAL;
    struct SerpentWindow *win = AS_POINTER(args[0]);
    int x = (int)AS_NUMBER(args[1]), y = (int)AS_NUMBER(args[2]);
    int w = (int)AS_NUMBER(args[3]), h = (int)AS_NUMBER(args[4]);
    const char* label = AS_CSTRING(args[5]);
    
    // Detection
    bool hovered = (win->mouse_x >= x && win->mouse_x <= x + w && win->mouse_y >= y && win->mouse_y <= y + h);
    uint32_t color = hovered ? (win->mouse_btn_left ? 0x606060 : 0x404040) : 0x303030;

    // Draw Background
    for (int j = y; j < y + h; j++) for (int i = x; i < x + w; i++) draw_pixel(win, i, j, color);

    int text_x = x + (w / 2) - (strlen(label) * 4);
    int text_y = y + (h / 2) - 8;
    Value textArgs[5];
    textArgs[0] = args[0];
    textArgs[1] = NUMBER_VAL(text_x);
    textArgs[2] = NUMBER_VAL(text_y);
    textArgs[3] = args[5];
    textArgs[4] = NUMBER_VAL(0xFFFFFF);
    wlDrawTextNative(5, textArgs);

    return BOOL_VAL(hovered && win->mouse_clicked);
}

Value wlTextBoxNative(int argCount, Value* args) {
    if (argCount < 7) return NIL_VAL;
    struct SerpentWindow *win = AS_POINTER(args[0]);
    int x = (int)AS_NUMBER(args[1]), y = (int)AS_NUMBER(args[2]);
    int w = (int)AS_NUMBER(args[3]), h = (int)AS_NUMBER(args[4]);
    const char* current = AS_CSTRING(args[5]);
    int id = (int)AS_NUMBER(args[6]);
    
    bool hovered = (win->mouse_x >= x && win->mouse_x <= x + w && win->mouse_y >= y && win->mouse_y <= y + h);
    if (hovered && win->mouse_clicked) {
        win->focused_id = id;
        printf("DEBUG: TextBox focused (ID %d)\n", id);
    }
    
    bool focused = (win->focused_id == id);
    uint32_t bg_color = focused ? 0x1A1A2E : 0x16213E;
    uint32_t border_color = focused ? 0xE94560 : 0x0F3460;

    for (int j = y; j < y + h; j++) {
        for (int i = x; i < x + w; i++) {
            if (i == x || i == x+w-1 || j == y || j == y+h-1) draw_pixel(win, i, j, border_color);
            else draw_pixel(win, i, j, bg_color);
        }
    }
    
    Value textArgs[] = { args[0], NUMBER_VAL(x + 5), NUMBER_VAL(y + (h/2) - 8), args[5], NUMBER_VAL(0xFFFFFF) };
    wlDrawTextNative(5, textArgs);

    if (focused && win->key_pressed) {
        printf("DEBUG: TextBox processing key (last_char: %c, key_code: %u)\n", win->last_char, win->last_key);
        if (win->last_key == 22) { // Backspace
            int len = strlen(current);
            if (len > 0) {
                char* new_str = malloc(len + 1);
                memcpy(new_str, current, len - 1);
                new_str[len - 1] = '\0';
                Value res = OBJ_VAL(copyString(new_str, len - 1));
                free(new_str);
                return res;
            }
        } else if (win->last_char >= 32 && win->last_char <= 126) {
            int len = strlen(current);
            char* new_str = malloc(len + 2);
            strcpy(new_str, current);
            new_str[len] = win->last_char;
            new_str[len + 1] = '\0';
            Value res = OBJ_VAL(copyString(new_str, len + 1));
            free(new_str);
            return res;
        }
    }
    
    return args[5];
}

Value wlCloseNative(int argCount, Value* args) {
    if (argCount != 1 || !IS_POINTER(args[0])) return NIL_VAL;
    struct SerpentWindow *win = AS_POINTER(args[0]);
    munmap(win->shm_data, win->width * win->height * 4);
    wl_buffer_destroy(win->buffer);
    if(win->pointer) wl_pointer_release(win->pointer);
    if(win->seat) wl_seat_release(win->seat);
    if(win->toplevel_decoration) zxdg_toplevel_decoration_v1_destroy(win->toplevel_decoration);
    xdg_toplevel_destroy(win->xdg_toplevel);
    xdg_surface_destroy(win->xdg_surface);
    wl_surface_destroy(win->surface);
    if(win->decoration_manager) zxdg_decoration_manager_v1_destroy(win->decoration_manager);
    xdg_wm_base_destroy(win->xdg_wm_base);
    wl_shm_destroy(win->shm);
    wl_compositor_destroy(win->compositor);
    wl_registry_destroy(win->registry);
    wl_display_disconnect(win->display);
    free(win);
    return NIL_VAL;
}
