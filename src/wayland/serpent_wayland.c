static void registry_handle_global(void *data, struct wl_registry *registry, uint32_t name, const char *interface, uint32_t version) {
    struct SerpentWindow *win = data;
    if (strcmp(interface, wl_compositor_interface.name) == 0) {
        win->compositor = wl_registry_bind(registry, name, &wl_compositor_interface, 4);
    } else if (strcmp(interface, wl_shm_interface.name) == 0) {
        win->shm = wl_registry_bind(registry, name, &wl_shm_interface, 1);
    } else if (strcmp(interface, xdg_wm_base_interface.name) == 0) {
        win->xdg_wm_base = wl_registry_bind(registry, name, &xdg_wm_base_interface, 1);
        xdg_wm_base_add_listener(win->xdg_wm_base, &xdg_wm_base_listener_impl, win);
    } else if (strcmp(interface, zxdg_decoration_manager_v1_interface.name) == 0) {
         win->decoration_manager = wl_registry_bind(registry, name, &zxdg_decoration_manager_v1_interface, 1);
    } else if (strcmp(interface, wl_seat_interface.name) == 0) {
        win->seat = wl_registry_bind(registry, name, &wl_seat_interface, 1);
        wl_seat_add_listener(win->seat, &seat_listener, win);
    }
}
static void registry_handle_global_remove(void *data, struct wl_registry *registry, uint32_t name) {}
static const struct wl_registry_listener registry_listener_impl = { .global = registry_handle_global, .global_remove = registry_handle_global_remove };

// Native Functions
Value wlCreateNative(int argCount, Value* args) {
    if (argCount < 2 || !IS_NUMBER(args[0]) || !IS_NUMBER(args[1])) return NIL_VAL;
    int width = (int)AS_NUMBER(args[0]), height = (int)AS_NUMBER(args[1]);
    const char* title = (argCount >= 3 && IS_STRING(args[2])) ? AS_CSTRING(args[2]) : "Serpent App";

    struct SerpentWindow *win = calloc(1, sizeof(struct SerpentWindow));
    win->width = width; win->height = height;
    win->display = wl_display_connect(NULL);
    if (!win->display) { free(win); return NIL_VAL; }

    win->registry = wl_display_get_registry(win->display);
    wl_registry_add_listener(win->registry, &registry_listener_impl, win);
    wl_display_roundtrip(win->display);

    win->surface = wl_compositor_create_surface(win->compositor);
    win->xdg_surface = xdg_wm_base_get_xdg_surface(win->xdg_wm_base, win->surface);
    xdg_surface_add_listener(win->xdg_surface, &xdg_surface_listener_impl, win);
    win->xdg_toplevel = xdg_surface_get_toplevel(win->xdg_surface);
    xdg_toplevel_add_listener(win->xdg_toplevel, &xdg_toplevel_listener_impl, win);
    xdg_toplevel_set_title(win->xdg_toplevel, title);

    if (win->decoration_manager) {
        win->toplevel_decoration = zxdg_decoration_manager_v1_get_toplevel_decoration(win->decoration_manager, win->xdg_toplevel);
        zxdg_toplevel_decoration_v1_set_mode(win->toplevel_decoration, ZXDG_TOPLEVEL_DECORATION_V1_MODE_SERVER_SIDE);
    }
    
    wl_surface_commit(win->surface);
    wl_display_roundtrip(win->display);

    int stride = width * 4, size = stride * height, fd = create_shm_file(size);
    win->shm_data = mmap(NULL, size, PROT_READ | PROT_WRITE, MAP_SHARED, fd, 0);
    struct wl_shm_pool *pool = wl_shm_create_pool(win->shm, fd, size);
    win->buffer = wl_shm_pool_create_buffer(pool, 0, width, height, stride, WL_SHM_FORMAT_XRGB8888);
    win->ready_to_draw = true;
    win->callback = NULL;

    win->xkb_context = xkb_context_new(XKB_CONTEXT_NO_FLAGS);
    win->focused_id = -1;

    return POINTER_VAL(win);
}

