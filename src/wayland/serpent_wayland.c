// SHM Logic
static int create_shm_file(off_t size) {
    int fd = syscall(SYS_memfd_create, "serpent-shm", MFD_CLOEXEC);
    if (fd < 0) return -1;
    if (ftruncate(fd, size) < 0) {
        close(fd);
        return -1;
    }
    return fd;
}

