// Keyboard Listeners
static void keyboard_handle_keymap(void *data, struct wl_keyboard *keyboard, uint32_t format, int fd, uint32_t size) {
    struct SerpentWindow *win = data;
    if (format != WL_KEYBOARD_KEYMAP_FORMAT_XKB_V1) { close(fd); return; }
    char *map_str = mmap(NULL, size, PROT_READ, MAP_SHARED, fd, 0);
    win->xkb_keymap = xkb_keymap_new_from_string(win->xkb_context, map_str, XKB_KEYMAP_FORMAT_TEXT_V1, 0);
    munmap(map_str, size);
    close(fd);
    win->xkb_state = xkb_state_new(win->xkb_keymap);
}

static void keyboard_handle_enter(void *data, struct wl_keyboard *keyboard, uint32_t serial, struct wl_surface *surface, struct wl_array *keys) {}
static void keyboard_handle_leave(void *data, struct wl_keyboard *keyboard, uint32_t serial, struct wl_surface *surface) {}

static void keyboard_handle_key(void *data, struct wl_keyboard *keyboard, uint32_t serial, uint32_t time, uint32_t key, uint32_t state) {
    struct SerpentWindow *win = data;
    if (state == WL_KEYBOARD_KEY_STATE_PRESSED) {
        win->key_pressed_pending = true;
        win->last_key_pending = key + 8; // Offset for XKB
        char buf[128];
        if (xkb_state_key_get_utf8(win->xkb_state, win->last_key_pending, buf, sizeof(buf)) > 0) {
            win->last_char_pending = buf[0];
        } else {
            win->last_char_pending = 0;
        }
        printf("DEBUG: Key Caught (Pending): %c\n", win->last_char_pending);
    }
}

static void keyboard_handle_modifiers(void *data, struct wl_keyboard *keyboard, uint32_t serial, uint32_t mods_depressed, uint32_t mods_latched, uint32_t mods_locked, uint32_t group) {
    struct SerpentWindow *win = data;
    xkb_state_update_mask(win->xkb_state, mods_depressed, mods_latched, mods_locked, 0, 0, group);
}

static const struct wl_keyboard_listener keyboard_listener = {
    .keymap = keyboard_handle_keymap,
    .enter = keyboard_handle_enter,
    .leave = keyboard_handle_leave,
    .key = keyboard_handle_key,
    .modifiers = keyboard_handle_modifiers,
};

static void seat_handle_capabilities(void *data, struct wl_seat *seat, uint32_t capabilities) {
    struct SerpentWindow *win = data;
    if (capabilities & WL_SEAT_CAPABILITY_POINTER) {
        win->pointer = wl_seat_get_pointer(win->seat);
        wl_pointer_add_listener(win->pointer, &pointer_listener, win);
    }
    if (capabilities & WL_SEAT_CAPABILITY_KEYBOARD) {
        win->keyboard = wl_seat_get_keyboard(win->seat);
        wl_keyboard_add_listener(win->keyboard, &keyboard_listener, win);
    }
}
static void seat_handle_name(void *data, struct wl_seat *seat, const char *name) {}

static const struct wl_seat_listener seat_listener = {
    .capabilities = seat_handle_capabilities,
    .name = seat_handle_name,
};

static void frame_callback_handler(void *data, struct wl_callback *callback, uint32_t time) {
    struct SerpentWindow *win = data;
    wl_callback_destroy(callback);
    win->callback = NULL;
    win->ready_to_draw = true;
}

static const struct wl_callback_listener frame_listener = {
    .done = frame_callback_handler,
};

// Utility: Draw Pixel
static inline void draw_pixel(struct SerpentWindow *win, int x, int y, uint32_t color) {
    if (x < 0 || x >= win->width || y < 0 || y >= win->height) return;
    uint32_t *p = (uint32_t*)win->shm_data;
    p[y * win->width + x] = color;
}

